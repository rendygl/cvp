<h2 dir="ltr">
    ANALISIS CVP (Cost Volume Profit)
</h2>
<hr>
<p dir="ltr">
    Diketahui : Penjualan = 5000 unit /bulan
</p>
<p dir="ltr">
    Harga (Sales Price) = $20/unit
</p>
<p dir="ltr">
    Harga Variabel (Variable Cost) = $12/unit
</p>
<p dir="ltr">
    Fixed Cost = $40000/bulan
</p>
<p dir="ltr">
    Jawab :
</p>
<br/>

