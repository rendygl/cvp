<h2 dir="ltr">
    Break Even Point
</h2>
<hr>
<p dir="ltr">
    BEP = Total Fix Cost / (Sales Price - Variable Cost)
</p>
<p dir="ltr">
    = 40000 / (20-12)
</p>
<p dir="ltr">
    = 40000 / 8
</p>
<p dir="ltr">
    = 5000
</p>
<h2 dir="ltr">
    Contribution Margin Approach
</h2>
<hr>
<p dir="ltr">
    CM = (CM per unit / harga per unit) * 100
</p>
<p dir="ltr">
    = (8/20) * 100
</p>
<p dir="ltr">
    = 40%
</p>
<br/>
<h2 dir="ltr">
    Jumlah Provit
</h2>
<hr>
<p dir="ltr">
    Target perusahaan = 30000. Berapa yang harus terjual?
</p>
<p dir="ltr">
    Unit sales = (Fixed Cost + Target perusahaan) / CM
</p>
<p dir="ltr">
    = (40000 + 30000) / 8
</p>
<p dir="ltr">
    = (70000) / 8
</p>
<p dir="ltr">
    = 8750 unit
</p>
<br/>
<p dir="ltr">
    Target Perusahaan = 30000. Berapa tingkat penjualan perusahaan?
</p>
<p dir="ltr">
    Sales = (Fixed Cost + Target perusahaan) / %CM
</p>
<p dir="ltr">
    = (40000 + 30000) / 40%
</p>
<p dir="ltr">
    = (70000) / 0.4
</p>
<p dir="ltr">
    = 175000
</p>
<br/>
<h2 dir="ltr">
    Changes in Fixed Cost
</h2>
<hr>
<p dir="ltr">
    Perusahaan menaikan fixed cost = 4000. Tentukan BEP terbaru!
</p>
<p dir="ltr">
    BEP = Fixed Cost / (Sales Price - Variable Cost)
</p>
<p dir="ltr">
    BEP Terbaru = (Fixed Cost + Tambahan Fixed Cost) / (Sales price - Variable
    Cost)
</p>
<p dir="ltr">
    = (40000 + 4000) / (20 - 12)
</p>
<p dir="ltr">
    = (44000) / (8)
</p>
<p dir="ltr">
    = 5500 unit
</p>
<br/>
<p dir="ltr">
    BEP $ = (Fixed Cost + Tambahan Fixed Cost) / %CM
</p>
<p dir="ltr">
    = (40000 + 4000) / 40%
</p>
<p dir="ltr">
    = (44000) / 0.4
</p>
<p dir="ltr">
    = $110000
</p>
<h2 dir="ltr">
    Changes in Variable Per Unit
</h2>
<hr>
<p dir="ltr">
    Perusahaan menaikkan harga variable per unit = 16. Tentukan BEP terbaru!
</p>
<p dir="ltr">
    BEP = Fixed Cost / (Sales Price - Variable Cost)
</p>
<p dir="ltr">
    BEP Terbaru = (Fixed Cost) / (Sales price - Variable Cost)
</p>
<p dir="ltr">
    = (40000) / (20 - 16)
</p>
<p dir="ltr">
    = (40000) / (4)
</p>
<p dir="ltr">
    = 10000 unit
</p>
<br/>
<p dir="ltr">
    %CM = ((Penjualan per unit - variable per unit) / Penjualan per unit ) *
    100%
</p>
<p dir="ltr">
    = ((20-16) / 20) * 100%
</p>
<p dir="ltr">
    = (4/20) * 100%
</p>
<p dir="ltr">
    = 20%
</p>
<br/>
<p dir="ltr">
    BEP $ = (Fixed Cost) / %CM
</p>
<p dir="ltr">
    = (40000) / 20%
</p>
<p dir="ltr">
    = (40000) / 0.2
</p>
<p dir="ltr">
    = $200000
</p>
<br/>
<h2 dir="ltr">
    Changes in sales price
</h2>
<hr>
<p dir="ltr">
    Perusahaan menaikkan sales price = 3. CM sebelumnya = 4 Tentukan BEP
    terbaru!
</p>
<p dir="ltr">
    BEP = Fixed Cost / (Sales Price - Variable Cost)
</p>
<p dir="ltr">
    BEP Terbaru = (Fixed Cost) / (Sales price - Variable Cost)
</p>
<p dir="ltr">
    = (40000) / (20 - 16)
</p>
<p dir="ltr">
    = (40000) / (4+3)
</p>
<p dir="ltr">
    = (40000) / (7)
</p>
<p dir="ltr">
    = 5175 unit
</p>
<br/>
<p dir="ltr">
    BEP $ = ((Fixed Cost) / ((CM per unit + 3) / (Sales Price + 3)))
</p>
<p dir="ltr">
    = (40000) / ((4+3) / (20+3))
</p>
<p dir="ltr">
    = (40000) / (7/23)
</p>
<p dir="ltr">
    = (40000) / (0.35)
</p>
<p dir="ltr">
    = $114286
</p>
<br/>
<h2 dir="ltr">
    Margin Of Safety
</h2>
<hr>
<p dir="ltr">
    Penjualan per tahun = 8000 unit
</p>
<p dir="ltr">
    Penjualan per bulan = 5000 unit
</p>
<p dir="ltr">
    Biaya tetap = 20
</p>
<p dir="ltr">
    Biaya variabel = 12
</p>
<p dir="ltr">
    MOS = (Penjualan per tahun - Penjualan per bulan) * biaya tetap
</p>
<p dir="ltr">
    = (8000 - 5000) * 20
</p>
<p dir="ltr">
    = (3000) * 20
</p>
<p dir="ltr">
    = 60000
</p>
